import streamlit as st
import joblib
import pandas as pd

# Cargar modelo entrenado
modelo = joblib.load("/content/drive/MyDrive/DATASET/modelo_rf_final.pkl")

st.set_page_config(page_title="Bienestar Laboral", layout="centered")

st.title("üå± Evaluaci√≥n de Bienestar Laboral")
st.write("Esta aplicaci√≥n usa Random Forest para predecir riesgos en salud mental.")

# Entradas del usuario
edad = st.number_input("Edad", 18, 100, 30)
horas_trabajo = st.slider("Horas de trabajo al d√≠a", 4, 16, 8)
estres = st.slider("Nivel de estr√©s (1 bajo - 10 alto)", 1, 10, 5)
satisfaccion = st.slider("Nivel de satisfacci√≥n laboral (1 bajo - 10 alto)", 1, 10, 5)

if st.button("üîç Evaluar"):
    datos = pd.DataFrame({
        "edad": [edad],
        "horas_trabajo": [horas_trabajo],
        "estres": [estres],
        "satisfaccion": [satisfaccion]
    })

    pred = modelo.predict(datos)[0]
    probas = modelo.predict_proba(datos)[0]

    if pred == 1:
        st.error(f"‚ö†Ô∏è Riesgo detectado (Probabilidad: {probas[1]:.2f})")
    else:
        st.success(f"‚úÖ Bajo riesgo (Probabilidad: {probas[0]:.2f})")

